def test_addition():

    num1 = 100
    num2 = 105
    sum = num1 + num2

    assert sum == 205

# Subtraction test
def test_subtraction():

    # Initialize two numbers
    num1 = 550
    num2 = 45

    # Subtract them
    diff = num1 - num2

    # Assertion
    assert diff == 505

# Multiplication test
def test_multiplication():

    # Initialize two numbers
    num1 = 25
    num2 = 10

    # Multiply them
    prod = num1 * num2

    # Assertion
    assert prod == 250

# Division test
def test_division():

    # Initialize two numbers
    num1 = 888
    num2 = 4

    # Divide them
    quot = num1 / num2

    # Assertion
    assert quot == 222